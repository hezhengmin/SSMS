--<algorithm>::= MD2 | MD4 | MD5 | SHA | SHA1 | SHA2_256 | SHA2_512
--傳回其在 SQL Server 中輸入的 MD2、MD4、MD5、SHA、SHA1 或 SHA2 雜湊。
DECLARE @HashThis NVARCHAR(32);  
SET @HashThis = CONVERT(NVARCHAR(32),'dslfdkjLK85kldhnv$n000#knf');  

-- SHA2_256 雜湊
SELECT HASHBYTES('SHA2_256', @HashThis); --0x9C4D6EA4E26587284D20D60A1C9CD10CA95AAD07D9BF0D8D51149C94A6B79D69

--轉成字串，去掉前面0x
SELECT CONVERT(NVARCHAR(100), HASHBYTES('SHA2_256', @HashThis),2);--9C4D6EA4E26587284D20D60A1C9CD10CA95AAD07D9BF0D8D51149C94A6B79D69
